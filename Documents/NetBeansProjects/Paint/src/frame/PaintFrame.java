    private void text_valueKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_valueKeyTyped
        // TODO add your handling code here:
        // TODO add your handling code here:
        int style = Font.PLAIN;
        if(italic_font.isSelected()){
            style = Font.ITALIC;
        }if(bold_font.isSelected()){
            style = Font.BOLD;
        }if(italic_font.isSelected() && bold_font.isSelected()){
            style = Font.BOLD | Font.ITALIC;
        }
        text_font = new Font(font_name.getSelectedItem().toString(), style,Integer.parseInt(sizes_combo.getSelectedItem().toString()));
        preview_text.setForeground(color);
        preview_text.setFont(text_font);
        String ff = text_value.getText();
        if(ff.length() > 15){
            ff = ff.substring(0,15);
        }
        preview_text.setText(ff);
    }//GEN-LAST:event_text_valueKeyTyped

