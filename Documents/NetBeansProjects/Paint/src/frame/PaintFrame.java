    private void save_asActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_asActionPerformed
        // TODO add your handling code here:
        JFileChooser jf = new JFileChooser(System.getProperty("user.home"));
        jf.showSaveDialog(this);
        if(jf.getSelectedFile() != null){
            String name = jf.getSelectedFile().getAbsolutePath();
            if(name.contains(".jpg")){
//                /int index = name.lastIndexOf(".jpg");
            }
            File path = new File(name);
            try {
                path.createNewFile();
                ImageIO.write(image, "jpeg", path);
            } catch (IOException ex) {
               // Logger.getLogger(PaintFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error on saving...", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_save_asActionPerformed

