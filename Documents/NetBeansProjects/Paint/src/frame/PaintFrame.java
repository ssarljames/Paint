    private int srcX = -1;
    private int srcY = -1;
    private void paintPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_paintPanelMouseClicked
        // TODO add your handling code here:
        Point p = evt.getPoint();
        paintPanelMouseMoved(evt);
        int dstX = p.x;
        int dstY = p.y;
       // graphics.drawOval(p.x, p.y, 10, 10);
        
       // System.out.println(srcX +"   "+ srcY+"   "+p.x+"   "+ p.y);
        
        int si = size.getValue()+8;
        if(pencil.isSelected()){
            graphics.fillOval(p.x-(si/2), p.y-(si/2), si,si);
        }
        else if(line.isSelected()){
            if(srcX > -1){
                graphics.drawLine(srcX , srcY,p.x, p.y);
                srcX = -1;
                srcY = -1;
            }else{
                srcX = p.x;
                srcY = p.y;
            }
        }else if(eclipse.isSelected()){
             if(srcX > -1){
                int x_delta = dstX - srcX;
                int y_delta = dstY - srcY;
                if(x_delta < 0){
                    x_delta *= -1;
                    srcX = dstX;
                }if(y_delta < 0){
                    y_delta *= -1;
                    srcY = dstY;
                }
                if(fill.isSelected()){
                    graphics.fillOval(srcX , srcY,x_delta,y_delta);
                }
                else{
                    graphics.drawOval(srcX , srcY,x_delta,y_delta);
                }
                srcX = -1;
                srcY = -1;
            }else{
                srcX = p.x;
                srcY = p.y;
            }
        }else if(rectangle.isSelected()){
            if(srcX > -1){
                int x_delta = dstX - srcX;
                int y_delta = dstY - srcY;
                if(x_delta < 0){
                    x_delta *= -1;
                    srcX = dstX;
                }if(y_delta < 0){
                    y_delta *= -1;
                    srcY = dstY;
                }
                if(fill.isSelected()){
                    graphics.fillRect(srcX , srcY,x_delta,y_delta);
                }
                else{
                    graphics.drawRect(srcX , srcY,x_delta,y_delta);
                }
                //graphics.drawRect(srcX , srcY,p.x, p.y);
                srcX = -1;
                srcY = -1;
            }else{
                srcX = p.x;
                srcY = p.y;
            }
        }else if(text.isSelected()){
           // String textv = JOptionPane.showInputDialog(this, "Enter Text: ");
           // graphics.setFont(new Font("Old English", si, si));
           // graphics.drawString(textv, dstX, dstY);
            srcX = p.x;
            srcY = p.y;
            text_value.setText("");
            Graphics.setToCenter(this, fontFormat);
            fontFormat.setVisible(true);
            text_value.grabFocus();
        }
        else{
            graphics.setPaint(Color.white);
            graphics.fillOval(p.x-(si/2), p.y-(si/2), si,si);
            graphics.setPaint(color);
        }
        
        updateImage();
    }//GEN-LAST:event_paintPanelMouseClicked

